;DISPLAY DIGITS 0 TO 9 WITH 1 SEC DELAY & 9 TO 0 WITH 1.5 SEC DELAY CONTINOUSLY 

ORG 70H
DB 3FH,06H,5BH,4FH,66H,6DH,7DH,07H,7FH,67H,67H,7FH,07H,7DH,6DH,66H,4FH,5BH,06H,3FH;LOOKUP TABLE VALUES
 ORG 00H
MOV P1,#00H
L10:MOV DPTR,#70H
    MOV R2,#0AH
L:  MOV A,#00H
    MOVC A,@A+DPTR
    INC DPTR 
    MOV P1,A
    LCALL DELAY1 ;1 SEC DELAY
DJNZ R2,L
MOV R2,#0AH
L1: MOV A,#00H
   MOVC A,@A+DPTR
   INC DPTR 
   MOV P1,A
   LCALL DELAY2 ;1.5 SEC DELAY
DJNZ R2,L1
SJMP L10
DELAY1:
      MOV TMOD,#10H ; TIMER 1 MODE 1
	  MOV R3,#10H ; GIVES 1 SEC DELAY ;REPEATS LOOP 16 TIMES
	  L3: MOV TL1,#00H ;lOWER BYTE OF COUNT
	      MOV TH1,#00H ;HIGHER BYTE OF COUNT
	      SETB TR1
	  L4: JNB TF1,L4 ;CHECKING TIMER OVERFLOW
		  CLR TR1
		  CLR TF1
	  DJNZ R3,L3
RET
DELAY2:
  MOV TMOD,#10H
	  MOV R4,#17H ;GIVES 1.5 SEC DELAY ;REPEATS LOOP 23 TIMES
	  L5: MOV TL1,#00H ;lOWER BYTE OF COUNT
	      MOV TH1,#00H ;HIGHER BYTE OF COUNT
	      SETB TR1
	  L6: JNB TF1,L6 ;CHECKING TIMER OVERFLOW
		  CLR TR1
		  CLR TF1
	  DJNZ R4,L5
RET
END